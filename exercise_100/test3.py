# 一个整数x，它加上100后是一个完全平方数，
# 再加上168又是一个完全平方数，请问该数是多少?

# x+100=m^2
# m^2+168=n^2
# n^2 - m^2 = 168 ; (n+m)(n-m)=168
# i=n+m ; j=n-m ;     i*j=168
# n=(i+j)/2 ; m=(i-j)/2 ;
# i,j都是>=2的偶数
# j >= 2 ; 1 < i < 168/2 + 1

for i in range(1, 85):
    if 168 % i == 0:
        j = 168 / i
        if i > j and (i+j) % 2 == 0 and (i-j) % 2 == 0:
            m = (i - j) / 2
            n = (i + j) / 2
            x = m * m - 100
            print(int(x))


